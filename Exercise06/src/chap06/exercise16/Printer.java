package chap06.exercise16;
/*
 * [16] PrinterExample 클래스에서 Printer 객체를 생성하고 println() 
 *  메소드를 호출해서 매개값을 콘솔에 출력하려고 합니다. println()메소드의 값으로는
 *  int, boolean,double,String,값을 줄 수 있습니다. Printer 클래스에서
 *  println() 메소드를 선언해보세요
 * 
 */

public class Printer {
	void println(int x) {
		System.out.println(x);
	}
	void println(boolean x) {
		System.out.println(x);
	}
	void println(double x) {
		System.out.println(x);
	}
	void println(String x) {
		System.out.println(x);
	}

}
